package gameProject;

public class Turret
{
	private int range;
	private int damage;
	private int cost;
	private int position;
	
	public int getRange()
	{
		return range;
	}
	
	public void setRange(int rnge)
	{
		this.range = rnge;
	}
	
	public int getDamage()
	{
		return damage;
	}
	
	public void setDamage(int dmge)
	{
		this.damage = dmge;
	}
	
	public int getCost ()
	{
		return cost;
	}
	
	public void setCost(int cst)
	{
		this.cost = cst;
	}
	
	public int getPosition()
	{
		return position;
	}
	
	public void setPosition(int psitin)
	{
		this.position = psitin;
	}
	
	
	
}
